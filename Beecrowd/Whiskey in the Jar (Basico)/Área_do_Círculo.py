""" A fórmula para calcular a área de uma circunferência é: area = π . raio^2. Considerando para este problema que π = 3.14159:

- Efetue o cálculo da área, elevando o valor de raio ao quadrado e multiplicando por π. """

pi = 3.14159
raio = float(input())
area = pi * (raio ** 2)

print(f"A={area:.4f}")